const ASK_USER_QUESTION_TOOL_NAME = 'AskUserQuestion'

export const EXIT_PLAN_MODE_V2_TOOL_PROMPT = `当你在计划模式中完成了向计划文件写入计划，并准备获得用户批准时使用此工具。

## 此工具的工作原理
- 你应该已经将计划写入了计划模式系统消息中指定计划文件
- 此工具不会将计划内容作为参数 - 它会从你写入的文件中读取计划
- 此工具只是表示你已完成计划，可以供用户审核和批准
- 用户在审核时会看到计划文件的内容

## 何时使用此工具
重要提示：仅当任务需要规划需要编写代码的任务实施步骤时使用此工具。对于研究任务（如收集信息、搜索文件、阅读文件或一般性地理解代码库），不要使用此工具。

## 使用此工具之前
确保你的计划完整且明确：
- 如果你对需求或方法有未解决的问题，先使用 ${ASK_USER_QUESTION_TOOL_NAME}（在早期阶段）
- 一旦你的计划确定，使用此工具请求批准

重要提示：不要使用 ${ASK_USER_QUESTION_TOOL_NAME} 询问"这个计划可以吗？"或"我应该继续吗？"——这正是此工具的作用。退出计划模式本身就是请求用户批准你的计划。

## 示例

1. 初始任务："搜索并理解代码库中 vim 模式的实现" - 不要使用退出计划模式工具，因为你不是在规划任务实施步骤。
2. 初始任务："帮助我实现 vim 的 yank 模式" - 在完成任务的实施步骤规划后使用退出计划模式工具。
3. 初始任务："添加新功能处理用户认证" - 如果不确定认证方法（OAuth、JWT 等），先使用 ${ASK_USER_QUESTION_TOOL_NAME}，然后在明确方法后使用退出计划模式工具。
`
