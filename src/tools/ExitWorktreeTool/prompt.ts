export function getExitWorktreeToolPrompt(): string {
  return `退出由 EnterWorktree 创建的工作树会话，并将会话返回到原始工作目录。

## 范围

此工具仅操作本次会话中由 EnterWorktree 创建的工作树。它不会影响：
- 你使用 \`git worktree add\` 手动创建的工作树
- 来自之前会话的工作树（即使是由 EnterWorktree 创建的）
- 如果 EnterWorktree 从未被调用，则不会影响你所在的目录

如果在 EnterWorktree 会话之外调用，此工具是一个**空操作**：它报告没有活动的工作树会话，不执行任何操作。文件系统状态不变。

## 何时使用

- 当用户明确要求"退出工作树"、"离开工作树"、"返回"或以其他方式结束工作树会话时
- 不要主动调用此工具——只有当用户要求时才调用

## 参数

- \`action\`（必需）：\`"keep"\` 或 \`"remove"\`
  - \`"keep"\` — 将工作树目录和分支保留在磁盘上。当用户想要以后再回来继续工作，或有需要保留的更改时使用。
  - \`"remove"\` — 删除工作树目录及其分支。当工作完成或被放弃时用于干净退出。
- \`discard_changes\`（可选，默认为 false）：仅在 \`action: "remove"\` 时有意义。如果工作树有未提交的文件或不在原始分支上的提交，工具将拒绝删除它，除非将其设置为 \`true\`。如果工具返回列出更改的错误，请先与用户确认，然后再用 \`discard_changes: true\` 重新调用。

## 行为

- 将会话的工作目录恢复到 EnterWorktree 之前的位置
- 清除依赖 CWD 的缓存（系统提示词部分、内存文件、计划目录），使会话状态反映原始目录
- 如果 tmux 会话附加到工作树：在 \`remove\` 时终止，在 \`keep\` 时保持运行（返回其名称，以便用户可以重新附加）
- 退出后，可以再次调用 EnterWorktree 来创建新的工作树
`
}
