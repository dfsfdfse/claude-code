export const DESCRIPTION = '更新任务列表中的任务'

export const PROMPT = `使用此工具更新任务列表中的任务。

## 何时使用此工具

**将任务标记为已完成：**
- 当你完成了任务中描述的工作时
- 当任务不再需要或已被取代时
- 重要提示：完成任务后始终将其标记为已完成
- 完成后，调用 TaskList 找到下一个任务

- 只有当你完全完成时才将任务标记为已完成
- 如果遇到错误、阻塞或无法完成，保持任务为 in_progress
- 被阻塞时，创建一个描述需要解决什么的新任务
- 以下情况不要将任务标记为已完成：
  - 测试失败
  - 实现不完整
  - 遇到未解决的错误
  - 找不到必要的文件或依赖

**删除任务：**
- 当任务不再相关或创建错误时
- 将状态设置为 \`deleted\` 会永久删除任务

**更新任务详情：**
- 当需求变更或更清晰时
- 在任务之间建立依赖关系时

## 可以更新的字段

- **status**：任务状态（见下方状态工作流）
- **subject**：更改任务标题（祈使语气，例如"运行测试"）
- **description**：更改任务描述
- **activeForm**：in_progress 时旋转器中显示的现在进行时形式（例如"正在运行测试"）
- **owner**：更改任务所有者（代理名称）
- **metadata**：将元数据键合并到任务中（将键设置为 null 以删除）
- **addBlocks**：标记在此任务完成之前无法开始的任务
- **addBlockedBy**：标记必须先完成才能开始此任务的任务

## 状态工作流

状态进展：\`pending\` → \`in_progress\` → \`completed\`

使用 \`deleted\` 永久删除任务。

## 注意事项

更新之前使用 \`TaskGet\` 读取任务的最新状态。

## 示例

开始工作时将任务标记为进行中：
\`\`\`json
{"taskId": "1", "status": "in_progress"}
\`\`\`

完成工作后将任务标记为已完成：
\`\`\`json
{"taskId": "1", "status": "completed"}
\`\`\`

删除任务：
\`\`\`json
{"taskId": "1", "status": "deleted"}
\`\`\`

通过设置 owner 来认领任务：
\`\`\`json
{"taskId": "1", "owner": "my-name"}
\`\`\`

设置任务依赖：
\`\`\`json
{"taskId": "2", "addBlockedBy": ["1"]}
\`\`\`
`
